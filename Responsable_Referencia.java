package hep.tramarefminsa;

public class Responsable_Referencia {

    private String idtipodocref;
    private String numdocref;
    private String apelpatrefiere;
    private String apelmatrefiere;
    private String nombperrefiere;
    private String fechanacrefiere;
    private String idcolegioref;
    private String idprofesionref;
    private String idsexorefiere;

    public Responsable_Referencia() {
    }

    public Responsable_Referencia(String idtipodocref, String numdocref, String apelpatrefiere, String apelmatrefiere, String nombperrefiere, String fechanacrefiere, String idcolegioref, String idprofesionref, String idsexorefiere) {
        this.idtipodocref = idtipodocref;
        this.numdocref = numdocref;
        this.apelpatrefiere = apelpatrefiere;
        this.apelmatrefiere = apelmatrefiere;
        this.nombperrefiere = nombperrefiere;
        this.fechanacrefiere = fechanacrefiere;
        this.idcolegioref = idcolegioref;
        this.idprofesionref = idprofesionref;
        this.idsexorefiere = idsexorefiere;
    }

    public String getIdtipodocref() {
        return idtipodocref;
    }

    public void setIdtipodocref(String idtipodocref) {
        this.idtipodocref = idtipodocref;
    }

    public String getNumdocref() {
        return numdocref;
    }

    public void setNumdocref(String numdocref) {
        this.numdocref = numdocref;
    }

    public String getApelpatrefiere() {
        return apelpatrefiere;
    }

    public void setApelpatrefiere(String apelpatrefiere) {
        this.apelpatrefiere = apelpatrefiere;
    }

    public String getApelmatrefiere() {
        return apelmatrefiere;
    }

    public void setApelmatrefiere(String apelmatrefiere) {
        this.apelmatrefiere = apelmatrefiere;
    }

    public String getNombperrefiere() {
        return nombperrefiere;
    }

    public void setNombperrefiere(String nombperrefiere) {
        this.nombperrefiere = nombperrefiere;
    }

    public String getFechanacrefiere() {
        return fechanacrefiere;
    }

    public void setFechanacrefiere(String fechanacrefiere) {
        this.fechanacrefiere = fechanacrefiere;
    }

    public String getIdcolegioref() {
        return idcolegioref;
    }

    public void setIdcolegioref(String idcolegioref) {
        this.idcolegioref = idcolegioref;
    }

    public String getIdprofesionref() {
        return idprofesionref;
    }

    public void setIdprofesionref(String idprofesionref) {
        this.idprofesionref = idprofesionref;
    }

    public String getIdsexorefiere() {
        return idsexorefiere;
    }

    public void setIdsexorefiere(String idsexorefiere) {
        this.idsexorefiere = idsexorefiere;
    }

    
}
